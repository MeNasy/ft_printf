#include "ft_printf.h"

void	ft_unputnbr(unsigned int nb, int *len)
{
	if (nb == 0)
		*len += write (1, "0", 1);
	else if (nb > 9)
	{
		ft_unputnbr(nb / 10, len);
		ft_unputnbr(nb % 10, len);
	}
	else
		ft_putchar(nb + '0', len);
}
