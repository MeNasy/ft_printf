#include "ft_printf.h"

void	ft_voidhx(unsigned long nb, int *len)
{
	char	*dizi;

	dizi = "0123456789abcdef";
	if (nb > 15)
	{
		ft_voidhx(nb / 16, len);
		ft_voidhx(nb % 16, len);
	}
	else
		ft_putchar(dizi[nb], len);
}
