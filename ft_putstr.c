#include "ft_printf.h"

void	ft_putstr(char *str, int *len)
{
	int	i;

	i = 0;
	if (!str)
	{
		*len += write (1, "(null)", 6);
		return ;
	}
	while (str[i])
	{
		*len += write (1, &str[i], 1);
		i++;
	}
}
